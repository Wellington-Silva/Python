op = input("Digite uma operação: ")
x = float(input("Digite um número: "))
y = float(input("Digite um número: "))
if (op == "+"):
    print(x + y)
elif (op == "-"):
    print(x - y)
elif (op == "*"):
    print(x * y)
elif (op == "/"):
    print(x / y)
else:
    print("Operação Inválida.")
    
